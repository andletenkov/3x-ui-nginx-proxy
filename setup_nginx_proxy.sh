generate_uuid() {
  if [[ -r /proc/sys/kernel/random/uuid ]]; then
    cat /proc/sys/kernel/random/uuid
  elif command -v uuidgen >/dev/null 2>&1; then
    uuidgen | tr '[:upper:]' '[:lower:]'
  elif command -v python3 >/dev/null 2>&1; then
    python3 -c 'import uuid; print(uuid.uuid4())'
  else
    die "No UUID generator available (need /proc/sys/kernel/random/uuid, uuidgen, or python3)."
  fi
}

install_3xui_and_inbounds() {
  local script_dir installer_out key value installer_script
  script_dir="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" &>/dev/null && pwd)"

  # INSTALL_3XUI_SCRIPT allows tests to stub the installer without touching
  # the real file. Not meant to be set outside of tests.
  installer_script="${INSTALL_3XUI_SCRIPT:-${script_dir}/install-3xui.sh}"

  [[ -x "$installer_script" ]] ||
    die "install-3xui.sh not found next to setup_nginx_proxy.sh (expected ${installer_script})."

  echo
  echo "=== Installing 3x-ui and configuring inbounds ==="
  echo "(3x-ui is the source of truth for its own username/password/port/path;"
  echo " they are generated by its installer, not dictated by this script.)"

  installer_out="$(
    PANEL_PORT="$PANEL_PORT" \
    WS_PORT="$WS_PORT" \
    WS_PATH="$WS_PATH" \
    GRPC_PORT="$GRPC_PORT" \
    GRPC_SERVICE="$GRPC_SERVICE" \
    SUB_PORT="$SUB_PORT" \
    SUB_PATH="$SUB_PATH" \
    CLIENT_UUID="$CLIENT_UUID" \
    XUI_VERSION="${XUI_VERSION:-}" \
    "$installer_script"
  )" || die "install-3xui.sh failed."

  while IFS='=' read -r key value; do
    case "$key" in
      PANEL_PORT) PANEL_PORT="$value" ;;
      PANEL_PATH) PANEL_PATH="$value" ;;
      XUI_USERNAME) XUI_USERNAME="$value" ;;
      XUI_PASSWORD) XUI_PASSWORD="$value" ;;
      CLIENT_UUID) CLIENT_UUID="$value" ;;
    esac
  done <<< "$installer_out"

  [[ -n "$PANEL_PORT" && -n "$PANEL_PATH" && -n "$XUI_USERNAME" && -n "$XUI_PASSWORD" && -n "$CLIENT_UUID" ]] ||
    die "install-3xui.sh did not report PANEL_PORT/PANEL_PATH/XUI_USERNAME/XUI_PASSWORD/CLIENT_UUID."

  validate_panel_port

  save_config
}

