verify_deployment() {
  local panel_domain="${PANEL_SUBDOMAIN}.${BASE_DOMAIN}"
  local vless_domain="${VLESS_SUBDOMAIN}.${BASE_DOMAIN}"
  local answer=""
  local all_ok=true

  echo
  echo "=== Post-configuration verification ==="
  echo
  echo "Now go configure 3x-ui / Xray to listen on:"
  echo "  Panel:  127.0.0.1:${PANEL_PORT}${PANEL_PATH}/"
  echo "  Sub:    127.0.0.1:${SUB_PORT}, path ${SUB_PATH}/"
  echo "  WS:     127.0.0.1:${WS_PORT}, path ${WS_PATH}"
  echo "  gRPC:   127.0.0.1:${GRPC_PORT}, serviceName ${GRPC_SERVICE}"
  echo
  read -r -p "Press Enter once 3x-ui/Xray are configured and running (or type 's' to skip verification): " answer

  if [[ "$answer" == "s" || "$answer" == "S" ]]; then
    echo "Skipping verification. Run this script's checks manually later if needed."
    return
  fi

  echo
  echo "Checking local listeners..."

  for check in "Panel:$PANEL_PORT" "Subscription:$SUB_PORT" "WebSocket:$WS_PORT" "gRPC:$GRPC_PORT"; do
    local check_name="${check%%:*}"
    local check_port="${check#*:}"

    if port_is_listening "$check_port"; then
      echo "  [OK]   ${check_name} is listening on 127.0.0.1:${check_port}"
    else
      echo "  [FAIL] ${check_name} is NOT listening on 127.0.0.1:${check_port}"
      all_ok=false
    fi
  done

  echo
  echo "Checking public HTTPS endpoints..."

  if command -v curl >/dev/null 2>&1; then
    local panel_status
    panel_status="$(curl -sk -o /dev/null -w '%{http_code}' --max-time 10 "https://${panel_domain}${PANEL_PATH}/" || echo "000")"

    if [[ "$panel_status" =~ ^(2|3)[0-9][0-9]$ ]]; then
      echo "  [OK]   https://${panel_domain}${PANEL_PATH}/ responded with HTTP ${panel_status}"
    else
      echo "  [FAIL] https://${panel_domain}${PANEL_PATH}/ responded with HTTP ${panel_status} (or was unreachable)"
      all_ok=false
    fi

    local vless_status
    vless_status="$(curl -sk -o /dev/null -w '%{http_code}' --max-time 10 "https://${vless_domain}/" || echo "000")"

    if [[ "$vless_status" =~ ^[0-9][0-9][0-9]$ && "$vless_status" != "000" ]]; then
      echo "  [OK]   TLS handshake to https://${vless_domain}/ succeeded (HTTP ${vless_status}, 404 is expected here)"
    else
      echo "  [FAIL] Could not complete a TLS handshake to https://${vless_domain}/"
      all_ok=false
    fi
  else
    echo "  [SKIP] curl not available for HTTPS checks."
  fi

  echo

  if [[ "$all_ok" == true ]]; then
    echo "All checks passed."
  else
    echo "Some checks failed. Verify your 3x-ui/Xray configuration matches the ports/paths above,"
    echo "then re-run the checks manually:"
    echo "  ss -lntp | egrep ':${PANEL_PORT}|:${SUB_PORT}|:${WS_PORT}|:${GRPC_PORT}'"
    echo "  curl -vk https://${panel_domain}${PANEL_PATH}/"
    echo "  curl -vk https://${vless_domain}/"
  fi
}

uninstall_all() {
  local script_dir installer_script answer
  script_dir="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" &>/dev/null && pwd)"
  installer_script="${INSTALL_3XUI_SCRIPT:-${script_dir}/install-3xui.sh}"

  require_root
  load_config

  echo "=== Uninstalling 3x-ui-nginx-proxy ==="
  echo "This removes: the Nginx site/Cloudflare-real-IP config, the Certbot"
  echo "deploy hook and certificate for '${BASE_DOMAIN:-<unknown -- none configured>}',"
  echo "the Cloudflare API token file, the UFW rules this script added, 3x-ui"
  echo "itself (service, binary, /etc/x-ui, /usr/local/x-ui), and this script's"
  echo "saved config/state files."
  echo
  read -r -p "Continue? [y/N]: " answer

  case "$answer" in
    y|Y|yes|YES) ;;
    *) echo "Cancelled."; exit 0 ;;
  esac

  echo
  echo "--- Nginx ---"
  rm -f "$NGINX_SITE_ENABLED" "$NGINX_SITE" "$CF_REAL_IP_CONF"
  if command -v nginx >/dev/null 2>&1; then
    nginx -t >/dev/null 2>&1 && (systemctl reload nginx || systemctl restart nginx) || true
  fi

  echo "--- Certbot ---"
  rm -f "$CERTBOT_DEPLOY_HOOK"
  if [[ -n "${BASE_DOMAIN:-}" ]] && command -v certbot >/dev/null 2>&1; then
    certbot delete --cert-name "$BASE_DOMAIN" --non-interactive >/dev/null 2>&1 || true
  fi
  rm -f "$CF_CREDENTIALS"

  echo "--- UFW ---"
  if command -v ufw >/dev/null 2>&1; then
    # SSH is never touched -- this script never added an SSH rule, so it
    # never removes one either (see configure_ufw).
    ufw delete allow 443/tcp >/dev/null 2>&1 || true
    ufw delete deny 80/tcp >/dev/null 2>&1 || true
    for p in "${PANEL_PORT:-}" "${SUB_PORT:-}" "${WS_PORT:-}" "${GRPC_PORT:-}"; do
      [[ -n "$p" ]] && ufw delete deny "${p}/tcp" >/dev/null 2>&1 || true
    done
    ufw reload >/dev/null 2>&1 || true
  fi

  echo "--- 3x-ui ---"
  if [[ -x "$installer_script" ]]; then
    "$installer_script" --uninstall || echo "WARNING: install-3xui.sh --uninstall reported an error; check manually." >&2
  else
    echo "WARNING: install-3xui.sh not found next to setup_nginx_proxy.sh (expected ${installer_script}); skipping 3x-ui removal." >&2
  fi

  echo "--- Local state ---"
  rm -f "$STATE_FILE" "$CONFIG_FILE"

  echo
  echo "Done. Uninstall complete."
}

